/* Array.prototype.shift

remove e retorna o primeiro item de um array.
*/

const plants = ["broccoli", "cauliflower", "tomato"];
const removedPlant = plants.shift();
console.log(removedPlant);
console.log(plants);

console.log(plants.shift());
console.log(plants);

console.log(plants.shift());
console.log(plants);

console.log(plants.shift());
console.log(plants);
